def build_smart_task(topic: str, assessment_type: str, item_type: str, index: int = 0, difficulty: str = "medium") -> SmartTaskDraft:
    clean_topic = normalize_topic(topic)
    family = infer_topic_family(clean_topic)
    bucket = _bucket_for_assessment(assessment_type, item_type)
    bank = QUESTION_BANK.get(family, DEFAULT_BANK)
    templates = bank.get(bucket) or bank.get("practice") or DEFAULT_BANK.get(bucket) or DEFAULT_BANK["practice"]
    text = templates[index % len(templates)].format(topic=clean_topic, topic_lower=clean_topic.lower())
    return SmartTaskDraft(
        topic=clean_topic,
        text=text,
        answer=_answer_for(clean_topic, family, bucket),
        criteria=_criteria_for(family, bucket),
        topic_family=family,
    )


def should_rebuild_text(text: str, answer: str = "") -> bool:
    joined = f"{text} {answer}".lower()
    return any(pattern in joined for pattern in GENERIC_TEXT_PATTERNS)


def normalize_topic(value: str) -> str:
    value = (value or "").replace("\ufffe", "-").replace("\u00ad", "")
    value = re.sub(r"^\s*Тема\s*\d+\.?\s*", "", value, flags=re.IGNORECASE)
    value = re.sub(r"\s+", " ", value).strip(" .;:-—\t\n\r")
    value = _collapse_repeated_prefix(value)
    value = _cut_explanatory_tail(value)
    value = re.sub(r"\s+", " ", value).strip(" .;:-—")
    return value[:140] or "Общие вопросы дисциплины"


def infer_topic_family(topic: str) -> str:
    text = topic.lower().replace("ё", "е")
    best_family = "generic"
    best_score = 0
    for family, keywords in FAMILY_KEYWORDS.items():
        score = 0
        for keyword in keywords:
            if keyword in text:
                score += 2 if len(keyword) > 8 else 1
        if score > best_score:
            best_family = family
            best_score = score
    if best_family == "generic" and any(word in text for word in ("по", "система", "проект", "программн")):
        return "software_engineering"
    return best_family


def _bucket_for_assessment(assessment_type: str, item_type: str) -> str:
    if assessment_type in {"exam_questions", "credit", "oral"}:
        return "oral"
    if assessment_type in {"practice", "exam_practice", "laboratory"}:
        return "practice"
    if assessment_type in {"control_work", "test_bank", "diagnostic"}:
        return "control_work" if assessment_type == "control_work" else "practice"
    if assessment_type in {"coursework", "course_project"} or item_type in {"coursework_topic", "project_topic"}:
        return "coursework"
    return "practice"


