def _collapse_repeated_prefix(value: str) -> str:
    words = value.split()
    for size in range(3, min(10, len(words) // 2 + 1)):
        left = " ".join(words[:size]).lower().strip(" .;:-—")
        right = " ".join(words[size: size * 2]).lower().strip(" .;:-—")
        if left and left == right:
            return " ".join(words[:size] + words[size * 2:])
    return value


def _cut_explanatory_tail(value: str) -> str:
    parts = re.split(r"\s+(?:Цель данной темы|В процессе изучения темы|На изучение|Описание инструментов|Основные методы|Технологии, используемые|Типы трудноформализуемых задач)\b", value, maxsplit=1, flags=re.IGNORECASE)
    value = parts[0]
    if "." in value:
        first = value.split(".", 1)[0].strip()
        if 2 <= len(first.split()) <= 8:
            return first
    return value
